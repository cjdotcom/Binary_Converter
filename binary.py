class Binary:
    def __init__(self):
        self.alfanum = {'A': 65, 'B': 66, 'C': 67, 'D': 68, 'E': 69, 'F': 70, 'G': 71,'H': 72, 'I': 73, 'J': 74,
                        'K': 75, 'L': 76, 'M': 77, 'N': 78, 'O': 79, 'P': 80, 'Q': 81, 'R': 82, 'S': 83, 'T': 84,
                        'U': 85, 'V': 86, 'X': 87, 'W': 88, 'Y': 89, 'Z': 90}

        self.alfabin = {'01000001': 0b1000001, '01000010': 0b1000010, '01000011': 0b1000011, '01000100': 0b1000100,
                        '01000101': 0b1000101, '01000110': 0b1000110, '01000111': 0b1000111, '01001000': 0b1001000,
                        '01001001': 0b1001001, '01001010': 0b1001010, '01001011': 0b1001011, '01001100': 0b1001100,
                        '01001101': 0b1001101, '01001110': 0b1001110, '01001111': 0b1001111, '01010000': 0b1010000,
                        '01010001': 0b1010001, '01010010': 0b1010010, '01010011': 0b1010011, '01010100': 0b1010100,
                        '01010101': 0b1010101, '01010110': 0b1010110, '01010111': 0b1010111, '01011000': 0b1011000,
                        '01011001': 0b1011001, '01011010': 0b1011010}

    def tobin(self, text):
        text = text.upper()
        for ch in text:
            if ch in self.alfanum:
                idx = self.alfanum.get(ch)
                print(f'0{bin(idx)[2:]}', end=' ')

    def totext(self, text):
        text = text
        lista = text.split()
        for ch in lista:
            if ch in self.alfabin:
                idx = self.alfabin.get(ch)
                print(chr(idx), end='')
